package com.poc.trainingmanager.utils;

public class CommonUtils {

	public static boolean isNull(Object obj) {
		if (obj==null)
			return true;
		return false;
	}
	
	public static boolean isStringNull(String str){
		if (str==null)
			return true;
		return false;
	}
	
	public static boolean isDigit(Integer i){
		if (isDigit(i))
			return true;
		return false;
	}

} 
